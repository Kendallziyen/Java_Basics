package control_statements;

public class Switch_case {

    public static void main(String[] args) {

        int day = 5;

        switch (day){

            case 1:
                System.out.println("Blue Monday");
                break;
            case 6:
                System.out.println("weekend");
                break;
            default:
                System.out.println("weekdays");
        }
    }
}

